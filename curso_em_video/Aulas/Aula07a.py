n = input('Qual o seu nome? ')
#inserido espaçoes de caracteres ou limitando
print(f'Prazer em te conhecer {n:20}!')
#alinhando a esquerda
print(f'Prazer em te conhecer {n:<20}!')
#alinhamdo a direita
print(f'Prazer em te conhecer {n:>20}!')
#centralisando
print(f'Prazer em te conhecer {n:^20}!')
#centralizando e ocupando os espaçoe em branco
print(f'Prazer em te conhecer {n:-^20}!')